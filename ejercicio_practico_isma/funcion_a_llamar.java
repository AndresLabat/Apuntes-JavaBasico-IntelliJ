package Java_Basico.ejercicio_practico_isma;

public class funcion_a_llamar {

    public static int suma(int a, int b, int c) {
        return a + b + c;

    }

    public static int resta(int a, int b, int c) {
        return a - b + c;

    }

    public static double sumasimple (double a, double b) {
        return a+b;

    }

}
